console.log(2 > 1);
console.log(2 < 1);

console.log(3 >= 2);
console.log(3 <= 3);
console.log(3 === 3);
console.log(3 !== 3);

console.log(true && false);
console.log(true || false);
console.log(!true);
console.log(!!false);
